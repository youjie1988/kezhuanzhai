package com.huaheng.mobilewms.bean;

public class TaskIds {

    private int taskId;

    public int getTaskId() {
        return taskId;
    }

    public void setTaskId(int taskId) {
        this.taskId = taskId;
    }

    @Override
    public String toString() {
        return "TaskDetail{" +
                "taskId=" + taskId +
                '}';
    }
}
